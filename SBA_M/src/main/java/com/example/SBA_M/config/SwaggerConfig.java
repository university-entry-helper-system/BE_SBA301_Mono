@SecurityScheme(
    name = "bearerAuth",
    type = SecuritySchemeType.HTTP,
    scheme = "bearer",
    bearerFormat = "JWT"
)
public class SwaggerConfig {
    // Nếu có method baseOpenAPI() chỉ cần trả về info, không cần security scheme ở đây
}
